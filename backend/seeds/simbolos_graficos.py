def seed_simbolos_graficos(db: Session, clinica_id: int) -> int:
    clinica_id = int(clinica_id or 0)
    if clinica_id <= 0:
        return 0
    if (
        db.query(SimboloGrafico.id)
        .filter(SimboloGrafico.clinica_id == clinica_id)
        .first()
        is not None
    ):
        return 0

    vistos_legacy: set[int] = set()
    vistos_codigo: set[str] = set()
    total = 0
    for row in SIMBOLOS_GRAFICOS_PADRAO:
        legacy_id = row.get("legacy_id")
        codigo_key = str(row.get("codigo") or "").strip().lower()

        # Chave de comparacao obrigatoria por legacy_id (sem fallback por codigo).
        legacy_int = int(legacy_id) if legacy_id is not None else None
        if legacy_int is not None and legacy_int in vistos_legacy:
            continue

        # Seed defensivo: evita codigos duplicados na carga inicial.
        if codigo_key and codigo_key in vistos_codigo:
            continue

        payload = {
            "clinica_id": clinica_id,
            "legacy_id": legacy_int,
            "codigo": row.get("codigo") or "",
            "descricao": row.get("descricao") or "",
            "especialidade": row.get("especialidade"),
            "tipo_marca": row.get("tipo_marca"),
            "tipo_simbolo": row.get("tipo_simbolo"),
            "bitmap1": row.get("bitmap1"),
            "bitmap2": row.get("bitmap2"),
            "bitmap3": row.get("bitmap3"),
            "icone": row.get("icone"),
            "imagem_custom": row.get("imagem_custom"),
            "sobreposicao": row.get("sobreposicao"),
            "ativo": bool(row.get("ativo") if row.get("ativo") is not None else True),
        }

        item = SimboloGrafico(**payload)
        db.add(item)
        if legacy_int is not None:
            vistos_legacy.add(legacy_int)
        if codigo_key:
            vistos_codigo.add(codigo_key)
        total += 1
    db.flush()
    return total
